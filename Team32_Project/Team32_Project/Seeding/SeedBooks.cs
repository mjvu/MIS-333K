                        db.Update(dbBook);
                        db.SaveChanges();
                    }
                }
            }
            catch
            {
                String msg = "Books added:" + intBooksAdded + "; Error on " + bookName;
                throw new InvalidOperationException(msg);
            }
        }
    }
